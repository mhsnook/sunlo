-- ============================================================================
alter table public.phrase_embedding enable row level security;

create policy "Anyone can read phrase embeddings" on public.phrase_embedding for
select
	using (true);

-- ============================================================================
-- RLS: phrase_embedding_queue is managed by service_role (triggers use SECURITY DEFINER)
-- ============================================================================
alter table public.phrase_embedding_queue enable row level security;

